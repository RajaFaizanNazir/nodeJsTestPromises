Date.prototype.getWeekNumber = function () {
  var d = new Date(
    Date.UTC(this.getFullYear(), this.getMonth(), this.getDate())
  );
  var dayNum = d.getUTCDay() || 7;
  d.setUTCDate(d.getUTCDate() + 4 - dayNum);
  var yearStart = new Date(Date.UTC(d.getUTCFullYear(), 0, 1));
  return Math.ceil(((d - yearStart) / 86400000 + 1) / 7);
};

const getDateName = (d1, d2) => {
  if (d1.getFullYear() === d2.getFullYear()) {
    if (d1.getDate() === d2.getDate() && d1.getMonth() === d2.getMonth()) {
      return "Today";
    }
    if (d1.getDate() - 1 === d2.getDate() && d1.getMonth() === d2.getMonth()) {
      return "Yesterday";
    }
    if (d1.getWeekNumber() === d2.getWeekNumber()) {
      return "Earlier This Week";
    }
    if (d1.getWeekNumber() - 1 === d2.getWeekNumber()) {
      return "Last Week";
    }
    if (d1.getMonth() === d2.getMonth()) {
      return "Earlier This Month";
    }
    if (d1.getMonth() - 1 === d2.getMonth()) {
      return "Last Month";
    }
    return "Earlier This Year";
  }
  if (d1.getFullYear() - 1 === d2.getFullYear()) {
    return "Last Year";
  }
  return "Long Time Ago";
};

